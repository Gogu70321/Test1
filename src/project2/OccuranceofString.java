package project2;

public class OccuranceofString {

	public static void main(String[] args) {
	
		

		String sy = "stsyuydtyisunsyuosuusiouso";
		char y = ' ';
	
		
		int length = sy.length();
		
		int count=0;
		
		for(int i = 0; i<length; i++ ) {
			
			if(sy.charAt(i)==y){
				
				count++;
			}
			
		}
	System.out.println("Count of String  :" +count);
}
	
	
	





}


